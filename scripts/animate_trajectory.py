#!/usr/bin/env python3
"""Animate optimization trajectory over an objective contour map."""

from __future__ import annotations

import argparse
import csv
import math
from pathlib import Path

import matplotlib

matplotlib.use("Agg", force=True)

import matplotlib.pyplot as plt
from matplotlib.animation import FuncAnimation
from matplotlib.animation import FFMpegWriter
from matplotlib.animation import PillowWriter
import numpy as np

from functions_2d import get_bounds, get_function, infer_bounds_from_points


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Create trajectory animation (GIF or MP4).")
    parser.add_argument("--history", required=True, help="CSV generated by extract_history.py.")
    parser.add_argument("--out", required=True, help="Output animation path (.gif or .mp4).")
    parser.add_argument("--function", default=None, help="Function name override.")
    parser.add_argument(
        "--bounds",
        nargs=4,
        type=float,
        metavar=("xmin", "xmax", "ymin", "ymax"),
        default=None,
        help="Override plotting bounds.",
    )
    parser.add_argument("--grid", type=int, default=300, help="Contour grid resolution.")
    parser.add_argument("--fps", type=int, default=20, help="Animation FPS.")
    parser.add_argument(
        "--max-frames",
        type=int,
        default=2000,
        help="Cap total frames for large histories by frame skipping (default: 2000).",
    )
    return parser.parse_args()


def as_float(value: str) -> float | None:
    if value is None:
        return None
    stripped = value.strip()
    if not stripped:
        return None
    try:
        number = float(stripped)
    except ValueError:
        return None
    return number if math.isfinite(number) else None


def load_history(path: Path) -> list[dict[str, str]]:
    with path.open("r", newline="", encoding="utf-8") as f:
        return list(csv.DictReader(f))


def infer_function(rows: list[dict[str, str]], override: str | None) -> str:
    if override:
        return override
    for row in rows:
        name = (row.get("function") or "").strip()
        if name:
            return name
    raise ValueError("Could not infer function from CSV. Use --function.")


def infer_bounds(
    rows: list[dict[str, str]], function_name: str, override: tuple[float, float, float, float] | None
) -> tuple[float, float, float, float]:
    if override is not None:
        return override
    try:
        return get_bounds(function_name)
    except ValueError:
        pass

    xs = [as_float(r.get("x", "")) for r in rows]
    ys = [as_float(r.get("y", "")) for r in rows]
    xs = [v for v in xs if v is not None]
    ys = [v for v in ys if v is not None]
    if not xs or not ys:
        raise ValueError("Cannot infer bounds from history without x/y values.")
    return infer_bounds_from_points(xs, ys)


def main() -> int:
    args = parse_args()
    rows = load_history(Path(args.history))
    if not rows:
        raise ValueError("History CSV is empty.")

    function_name = infer_function(rows, args.function)
    # Centralize formulas so animations stay consistent with the evaluator registry.
    fn = get_function(function_name)

    bounds_override = tuple(args.bounds) if args.bounds is not None else None
    xmin, xmax, ymin, ymax = infer_bounds(rows, function_name, bounds_override)

    xs: list[float] = []
    ys: list[float] = []
    vals: list[float] = []
    for row in rows:
        x = as_float(row.get("x", ""))
        y = as_float(row.get("y", ""))
        v = as_float(row.get("objective", ""))
        if x is None or y is None:
            continue
        xs.append(x)
        ys.append(y)
        vals.append(float("nan") if v is None else v)

    if not xs:
        raise ValueError("No valid x/y points found in history.")

    x_arr = np.asarray(xs, dtype=float)
    y_arr = np.asarray(ys, dtype=float)
    v_arr = np.asarray(vals, dtype=float)

    x_grid = np.linspace(xmin, xmax, args.grid)
    y_grid = np.linspace(ymin, ymax, args.grid)
    xx, yy = np.meshgrid(x_grid, y_grid)
    zz = fn(xx, yy)

    fig, ax = plt.subplots(figsize=(9, 7))
    contour = ax.contourf(xx, yy, zz, levels=50)
    fig.colorbar(contour, ax=ax, label="objective")

    line, = ax.plot([], [], linewidth=1.2, label="trajectory")
    points = ax.scatter([], [], s=10, alpha=0.8, label="evaluations")
    best_marker = ax.scatter([], [], marker="*", s=180, label="best-so-far")

    ax.set_xlabel("x")
    ax.set_ylabel("y")
    ax.set_xlim(xmin, xmax)
    ax.set_ylim(ymin, ymax)
    ax.set_title(f"{function_name} trajectory")
    ax.legend(loc="best")

    finite_values = np.where(np.isfinite(v_arr), v_arr, np.inf)
    best_idx_so_far = np.minimum.accumulate(finite_values).argmin()  # fallback for empty finite

    total = x_arr.size
    step = max(1, int(math.ceil(total / max(1, args.max_frames))))
    frame_indices = np.arange(1, total + 1, step, dtype=int)
    if frame_indices[-1] != total:
        frame_indices = np.append(frame_indices, total)

    best_until: list[int] = []
    current_best = float("inf")
    current_best_idx = 0
    for idx, value in enumerate(finite_values):
        if value < current_best:
            current_best = value
            current_best_idx = idx
        best_until.append(current_best_idx)

    def init() -> tuple:
        line.set_data([], [])
        points.set_offsets(np.empty((0, 2)))
        best_marker.set_offsets(np.empty((0, 2)))
        return (line, points, best_marker)

    def update(frame_number: int) -> tuple:
        n = frame_indices[frame_number]
        line.set_data(x_arr[:n], y_arr[:n])
        points.set_offsets(np.column_stack((x_arr[:n], y_arr[:n])))
        bidx = best_until[n - 1] if n > 0 else best_idx_so_far
        best_marker.set_offsets(np.array([[x_arr[bidx], y_arr[bidx]]]))
        return (line, points, best_marker)

    anim = FuncAnimation(
        fig,
        update,
        init_func=init,
        frames=frame_indices.size,
        interval=max(1, int(round(1000.0 / max(1, args.fps)))),
        blit=True,
    )

    out_path = Path(args.out)
    out_path.parent.mkdir(parents=True, exist_ok=True)

    suffix = out_path.suffix.lower()
    if suffix == ".gif":
        writer = PillowWriter(fps=args.fps)
    elif suffix == ".mp4":
        writer = FFMpegWriter(fps=args.fps)
    else:
        raise ValueError("Output extension must be .gif or .mp4")

    try:
        anim.save(out_path, writer=writer)
    except Exception as exc:
        if suffix == ".mp4":
            raise RuntimeError(
                "Failed to write MP4. Ensure ffmpeg is installed and on PATH, "
                "or use a .gif output path."
            ) from exc
        raise

    print(f"Saved animation: {out_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
