#!/usr/bin/env python3
"""Plot contour surface and optimization trajectory from history.csv."""

from __future__ import annotations

import argparse
import csv
import math
from pathlib import Path

import numpy as np

from functions_2d import get_bounds, get_function, infer_bounds_from_points


def preparse_out_path() -> str | None:
    parser = argparse.ArgumentParser(add_help=False)
    parser.add_argument("--out", default=None)
    args, _ = parser.parse_known_args()
    return args.out


if preparse_out_path():
    import matplotlib

    matplotlib.use("Agg", force=True)

import matplotlib.pyplot as plt


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Plot trajectory over objective contour.")
    parser.add_argument("--history", required=True, help="CSV generated by extract_history.py.")
    parser.add_argument("--function", default=None, help="Function name override.")
    parser.add_argument(
        "--bounds",
        nargs=4,
        type=float,
        metavar=("xmin", "xmax", "ymin", "ymax"),
        default=None,
        help="Override plotting bounds.",
    )
    parser.add_argument("--grid", type=int, default=400, help="Contour grid resolution.")
    parser.add_argument("--out", default=None, help="PNG output path. If omitted, show interactively.")
    parser.add_argument("--title", default=None, help="Plot title.")
    parser.add_argument(
        "--best-so-far",
        action=argparse.BooleanOptionalAction,
        default=True,
        help="Mark current best point and best-so-far updates.",
    )
    parser.add_argument(
        "--show-points",
        action=argparse.BooleanOptionalAction,
        default=True,
        help="Show scatter points for all evaluations.",
    )
    parser.add_argument(
        "--show-path",
        action=argparse.BooleanOptionalAction,
        default=True,
        help="Show line path connecting evaluations.",
    )
    return parser.parse_args()


def as_float(value: str) -> float | None:
    if value is None:
        return None
    stripped = value.strip()
    if not stripped:
        return None
    try:
        number = float(stripped)
    except ValueError:
        return None
    return number if math.isfinite(number) else None


def load_history(path: Path) -> list[dict[str, str]]:
    with path.open("r", newline="", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        return list(reader)


def infer_function(rows: list[dict[str, str]], override: str | None) -> str:
    if override:
        return override
    for row in rows:
        name = (row.get("function") or "").strip()
        if name:
            return name
    raise ValueError(
        "Could not infer function from CSV. Use --function to specify one explicitly."
    )


def infer_bounds(
    rows: list[dict[str, str]], function_name: str, override: tuple[float, float, float, float] | None
) -> tuple[float, float, float, float]:
    if override is not None:
        return override

    try:
        return get_bounds(function_name)
    except ValueError:
        pass

    xs = [as_float(r.get("x", "")) for r in rows]
    ys = [as_float(r.get("y", "")) for r in rows]
    xs = [v for v in xs if v is not None]
    ys = [v for v in ys if v is not None]
    if not xs or not ys:
        raise ValueError("Cannot infer bounds from history: missing x/y values.")
    return infer_bounds_from_points(xs, ys)


def main() -> int:
    args = parse_args()
    history_path = Path(args.history)
    rows = load_history(history_path)
    if not rows:
        raise ValueError("History CSV is empty.")

    function_name = infer_function(rows, args.function)
    # Centralize formulas so plots stay consistent with the evaluator registry.
    fn = get_function(function_name)

    bounds_override = tuple(args.bounds) if args.bounds is not None else None
    xmin, xmax, ymin, ymax = infer_bounds(rows, function_name, bounds_override)

    x_grid = np.linspace(xmin, xmax, args.grid)
    y_grid = np.linspace(ymin, ymax, args.grid)
    xx, yy = np.meshgrid(x_grid, y_grid)
    zz = fn(xx, yy)

    xs: list[float] = []
    ys: list[float] = []
    vals: list[float] = []
    for row in rows:
        x = as_float(row.get("x", ""))
        y = as_float(row.get("y", ""))
        v = as_float(row.get("objective", ""))
        if x is None or y is None:
            continue
        xs.append(x)
        ys.append(y)
        vals.append(float("nan") if v is None else v)

    if not xs:
        raise ValueError("No valid x/y points found in history.")

    x_arr = np.asarray(xs, dtype=float)
    y_arr = np.asarray(ys, dtype=float)
    v_arr = np.asarray(vals, dtype=float)

    fig, ax = plt.subplots(figsize=(9, 7))
    contour = ax.contourf(xx, yy, zz, levels=50)
    fig.colorbar(contour, ax=ax, label="objective")

    if args.show_path:
        ax.plot(x_arr, y_arr, linewidth=1.2, label="trajectory")

    if args.show_points:
        ax.scatter(x_arr, y_arr, s=10, alpha=0.7, label="evaluations")

    if args.best_so_far and np.isfinite(v_arr).any():
        best_idx = int(np.nanargmin(v_arr))
        ax.scatter(
            [x_arr[best_idx]],
            [y_arr[best_idx]],
            marker="*",
            s=180,
            label=f"best ({v_arr[best_idx]:.6g})",
        )

        # Plot only points where a new best objective was found.
        best_so_far = np.minimum.accumulate(np.where(np.isfinite(v_arr), v_arr, np.inf))
        improving = np.r_[True, np.diff(best_so_far) < 0]
        bx = x_arr[improving]
        by = y_arr[improving]
        if bx.size >= 2:
            ax.plot(bx, by, linestyle="--", linewidth=1.0, label="best-so-far updates")

    ax.set_xlabel("x")
    ax.set_ylabel("y")
    ax.set_xlim(xmin, xmax)
    ax.set_ylim(ymin, ymax)
    ax.set_title(args.title or f"{function_name} trajectory")
    ax.legend(loc="best")
    fig.tight_layout()

    if args.out:
        out_path = Path(args.out)
        out_path.parent.mkdir(parents=True, exist_ok=True)
        fig.savefig(out_path, dpi=160)
        print(f"Saved plot: {out_path}")
    else:
        plt.show()

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
